local imgs = {}
local n = 0
for _, img in pairs(images) do
    n = n + 1
    imgs[n] = img
end

local i = 1

curr_img = imgs[i]
layer:Insert(curr_img, 1)

function lt.KeyDown(key)
    if key == "right" then
        i = i + 1
    elseif key == "left" then
        i = i - 1
    end
    if i > n then
        i = 1
    elseif i < 1 then
        i = n
    end
    layer:Remove(curr_img)
    curr_img = imgs[i]
    layer:Insert(curr_img, 1)
end

local main_layer = layer
layer:Insert(lt.Tint(lt.Rect(-10, -10, 10, 10), 0.5, 0.5, 0.5), 0);

function lt.Advance()
end

function lt.Render()
    main_layer:Draw()
end
